def add_numbers(a, b):
    """
    Функция складывает два числа.

    :param a: Первое число.
    :param b: Второе число.
    :return: Сумма двух чисел.
    """
    return a + b

def subtract_numbers(a, b):
    """
    Функция вычитает второе число из первого.

    :param a: Первое число.
    :param b: Второе число.
    :return: Разность двух чисел.
    """
    return a - b

